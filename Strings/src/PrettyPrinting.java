public class PrettyPrinting {
    public static void main(String[] args) {
        float num = 431.1287f;
        System.out.printf("Formatted number is %.2f", num);
        System.out.printf("Pie: %.3f", Math.PI);

        //strings
        System.out.printf("Hello I am %s and I am %s", "Kunal", "cool");
    }
}
